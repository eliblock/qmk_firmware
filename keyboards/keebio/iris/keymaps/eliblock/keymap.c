bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  if (record->event.pressed) {
    #ifdef RGBLIGHT_ENABLE
      if ( board_idle ) {
        // Unsuspend features as desired
        if ( rgblight_suspended ) {
          rgblight_toggle_noeeprom();
          rgblight_suspended = false;
        }
        // Configure active state
        board_idle = false;
      }

      // Reset the idle timer
      idle_timer = timer_read();
      thirty_second_idle_counter = 0;
    #endif
  }

  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
      break;
    case CTRALTCMD:
      if (record->event.pressed) {
        SEND_STRING(SS_DOWN(X_LCTL) SS_DOWN(X_LALT) SS_DOWN(X_LGUI));
      } else {
        SEND_STRING(SS_UP(X_LCTL) SS_UP(X_LALT) SS_UP(X_LGUI));
      }
      return true;
      break;
    case CTRCMD:
      if (record->event.pressed) {
        SEND_STRING(SS_DOWN(X_LCTL) SS_DOWN(X_LGUI));
      } else {
        SEND_STRING(SS_UP(X_LCTL) SS_UP(X_LGUI));
      }
      return true;
      break;
    case CTRALT:
      if (record->event.pressed) {
        SEND_STRING(SS_DOWN(X_LCTL) SS_DOWN(X_LALT));
      } else {
        SEND_STRING(SS_UP(X_LCTL) SS_UP(X_LALT));
      }
      return true;
      break;
  }
  return true;
}

void encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) {
        if (clockwise) {
            tap_code(KC_VOLU);
        } else {
            tap_code(KC_VOLD);
        }
    }
    else if (index == 1) {
        if (clockwise) {
            tap_code(KC_PGDN);
        } else {
            tap_code(KC_PGUP);
        }
    }
}

void matrix_scan_user(void) {
  #ifdef RGBLIGHT_ENABLE
    // initialize default values on first scan
    if (idle_timer == 0) {
      idle_timer = timer_read();
    }

    // Count every 30 seconds of inactivity while the board is not idle
    if ( !board_idle && timer_elapsed(idle_timer) > 30000) {
      thirty_second_idle_counter++;
      idle_timer = timer_read();
    }

    // If the timeout has elapsed, set the board to idle
    if ( !board_idle && thirty_second_idle_counter >= IDLE_TIMEOUT * 2) {
      // Suspend features as desired
      if ( rgblight_is_enabled() ) {
        rgblight_toggle_noeeprom();
        rgblight_suspended = true;
      }
      // Configure idle state
      board_idle = true;
      thirty_second_idle_counter = 0;
    }
  #endif
}
